# #使用函数嵌套

# #选出a,b,c 中最大的数
def dod(a,b,c):
    def findmax(a,b):
        if a>b:
            return a
        else:
            return b
    m=findmax(a,b)
    m=findmax(m,c)
    return m

print("1,2,3中最大的数为",dod(1,2,3))




