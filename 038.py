#默认函数

#调用函数时，可以省略在定义中指定了默认值的参数

#指定一个默认值的函数
def doa(a,b=2):
    return a*b

print(doa(2))   #省略了指定默认值的第二个参数
print(doa(2,3))     #也可以像一般函数一样不省略参数

#指定默认值只能够从最后一个参数往前面练血的指定若干个，不能在中间间隔指定

# #错误的指定方法
# def dob(a,b=2,c):
#     print("a="+a+"b="+b+"c="+c)

#正确的指定方法
def dob(a,b=2,c=1):
    print("a="+a+"b="+b+"c="+c)


